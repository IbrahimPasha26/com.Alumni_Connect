package com.Alumni_Connect.User;

public class Alumni2 {
    private String name;
    private String email;
    private int yearOfGraduation;
    private String currentJobTitle;
    private String currentCompany;
    private String profilePicture;

    // Getters and setters
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public int getYearOfGraduation() { return yearOfGraduation; }
    public void setYearOfGraduation(int yearOfGraduation) { this.yearOfGraduation = yearOfGraduation; }

    public String getCurrentJobTitle() { return currentJobTitle; }
    public void setCurrentJobTitle(String currentJobTitle) { this.currentJobTitle = currentJobTitle; }

    public String getCurrentCompany() { return currentCompany; }
    public void setCurrentCompany(String currentCompany) { this.currentCompany = currentCompany; }

    public String getProfilePicture() { return profilePicture; }
    public void setProfilePicture(String profilePicture) { this.profilePicture = profilePicture; }
}
